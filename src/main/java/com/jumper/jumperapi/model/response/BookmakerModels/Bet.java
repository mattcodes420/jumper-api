package com.jumper.jumperapi.model.response.BookmakerModels;

import java.util.List;

public class Bet {
    private int id;
    private String name;
    private List<Value> values;

    // Getters and Setters
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public List<Value> getValues() {
        return values;
    }

    public void setValues(List<Value> values) {
        this.values = values;
    }
}
